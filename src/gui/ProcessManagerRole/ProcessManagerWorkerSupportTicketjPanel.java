    private void enterprisessjTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_enterprisessjTableMouseClicked
       int selectedRow = enterprisessjTable.getSelectedRow();
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Select No Ticket Selected.");
            ticketjTextArea.setText("");
            return;
        }
        SupportTicketRequestWorker ticket = (SupportTicketRequestWorker)enterprisessjTable.getValueAt(selectedRow, 0);
        ticketjTextArea.setText(ticket.getMessage());
    }//GEN-LAST:event_enterprisessjTableMouseClicked

    private void submitTicketjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitTicketjButtonActionPerformed
        String ticket = ticketjTextArea.getText().trim();
       
        if(ticket.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Specify Ticket", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(ticket.length()<=5) {
            JOptionPane.showMessageDialog(this, "Ticket must be Five characters and more", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(ticket.length()>400) {
            JOptionPane.showMessageDialog(this, "Ticket should have less than 400 characters", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        int confirm=JOptionPane.showConfirmDialog(null, "Send Support Ticket?", "Support Ticket", JOptionPane.YES_NO_OPTION);
        if(confirm==0){
            SupportTicketRequestWorker request = new SupportTicketRequestWorker();
            request.setMessage(ticket);
            request.setSender(user);
            request.setStatus("Ticket Submitted");
            Department itOrg=null;
            for(Network network:system.getNetworkList()){
                    for(Enterprise enterprises:network.getEnterpriseDirectory().getEnterpriseList()){
                        for(Department depart : enterprises.getDepartmentDirectory().getDepartmentList()){
                            if (depart instanceof ITDepartment){
                                itOrg = depart;
                                break;
                            }
                        }

                    }
                }

                if(itOrg==null){
                    JOptionPane.showMessageDialog(null, "Refund Request Not Sent");
                    return;
                }
            itOrg.getWorkQueue().getWorkRequestList().add(request);
            user.getWorkQueue().getWorkRequestList().add(request);
            ticketjTextArea.setText("");
            JOptionPane.showMessageDialog(null, "Ticket Submitted.\nA Please Wait for responses.");
            dB4OModel.storeSystem(system);
            displayTickets();
        }
        
    }                                


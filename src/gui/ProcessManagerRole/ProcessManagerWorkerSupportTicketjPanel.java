    private void displayTickets(){
        DefaultTableModel model = (DefaultTableModel) enterprisessjTable.getModel();
        
        model.setRowCount(0);
        for (WorkRequest order : user.getWorkQueue().getWorkRequestList()){
            Object[] row = new Object[5];
            String request=String.valueOf(order);
            if(request.contains("Controller.WorkQueue.SupportTicketRequestWorker")){
                row[0] = order;
                row[1] = (order.getReceiver()!=null)?order.getReceiver():"";
                row[2] = order.getMessage();
                row[3] = order.getStatus();
                String result = ((SupportTicketRequestWorker)order).getRequesteResult();
                row[4] = result == null ? "Waiting" : result;
                model.addRow(row);
            }
        }
    }
    


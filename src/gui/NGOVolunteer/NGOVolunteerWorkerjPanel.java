    private void requestFundsjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_requestFundsjButtonActionPerformed
        String fundsMessage = fundsMessagejTextField.getText().trim();
        String fundsAmount =fundsAmountjTextField.getText().trim();
        double amount;
        if(fundsMessage.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Specify Fund Message Name", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(fundsMessage.length()<=3) {
            JOptionPane.showMessageDialog(this, "Fund Message must be Three characters and more", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(fundsMessage.length()>100) {
            JOptionPane.showMessageDialog(this, "Fund Message should have less than 100 characters", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        try{
            amount=Double.parseDouble(fundsAmount);
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(this, "Fund Amount Must be a Positive Integer Number.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int confirm=JOptionPane.showConfirmDialog(null, "Send Funds Request?", "Funds Request", JOptionPane.YES_NO_OPTION);
        if(confirm==0){
        FundsRequestWorker request = new FundsRequestWorker();
        request.setMessage(fundsMessage);
        request.setAmount(amount);
        request.setSender(user);
        request.setStatus("Funds Requested");
        Department operationsOrg=null;
        for(Network network:system.getNetworkList()){
                for(Enterprise enterprises:network.getEnterpriseDirectory().getEnterpriseList()){
                    for(Department depart : enterprises.getDepartmentDirectory().getDepartmentList()){
                        if (depart instanceof OperationsDepartment){
                            operationsOrg = depart;
                            break;
                        }
                    }

                }
            }

            if(operationsOrg==null){
                JOptionPane.showMessageDialog(null, "Refund Request Not Sent");
                return;
            }
        operationsOrg.getWorkQueue().getWorkRequestList().add(request);
        user.getWorkQueue().getWorkRequestList().add(request);
        JOptionPane.showMessageDialog(null, "Funds Request Submitted.\nA Please Wait for responses.");
        dB4OModel.storeSystem(system);
        displayRequestedFunds();
        }
    }                                


    public void displayRequestedFunds(){
        DefaultTableModel model = (DefaultTableModel) requestedFundsjTable.getModel();
        
        model.setRowCount(0);
        
        for (WorkRequest order : user.getWorkQueue().getWorkRequestList()){
            Object[] row = new Object[6];
            String request=String.valueOf(order);
            if(request.contains("Controller.WorkQueue.FundsRequestWorker")){
                row[0] = order;
                row[1] = (order.getReceiver()!=null)?order.getReceiver():"";
                row[2] = order.getMessage();
                row[3] = ((FundsRequestWorker)order).getAmount();
                row[4] = order.getStatus();
                String result = ((FundsRequestWorker)order).getRequesteResult();
                row[5] = result == null ? "Waiting" : result;
                model.addRow(row);
            }
        }
    }
    

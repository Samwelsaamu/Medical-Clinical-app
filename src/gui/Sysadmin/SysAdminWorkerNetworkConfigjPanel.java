    private void saveNetworkjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveNetworkjButtonActionPerformed
        String networkName = networkNamejTextField.getText().trim();
        String networkEmail = networkMailjTextField.getText().trim();
        String networkEPass = networkMPassjTextField.getText().trim();
        String networkSMTPH = networkMSMTPHjTextField.getText().trim();
        String networkSMTPP = networkMSMTPPjTextField.getText().trim();
        if(networkName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Network Name is Required", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(networkName.length()<=10) {
            JOptionPane.showMessageDialog(this, "Please Enter A Network Name with more than 10 Characters", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(networkName.length()>120) {
            JOptionPane.showMessageDialog(this, "Please Specify Network Name with less than 120 Characters", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        if(networkEmail.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Network Email User is Required", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(networkEPass.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Network Email Password is Required", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(networkSMTPH.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Network Email SMTP HOST is Required", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(networkSMTPP.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Network Email SMTP PORT is Required", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        if(!system.checkIfNetworkIsUnique(networkName)){
            JOptionPane.showMessageDialog(this, "This Network Exists", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Network newNetwork = system.createNewNetwork();
        newNetwork.setName(networkName);
        newNetwork.setSender(networkEmail);
        newNetwork.setEmailPassword(networkEPass);
        newNetwork.setSmtpHost(networkSMTPH);
        newNetwork.setSmtpPort(networkSMTPP);
        dB4OModel.storeSystem(system);
        DisplayNetworks();
    }//GEN-LAST:event_saveNetworkjButtonActionPerformed

    private void deleteNetworkjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteNetworkjButtonActionPerformed
        int selectedRow = this.networksjTable.getSelectedRow();
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Select Network or Add.");
            this.networkNamejTextField.setText("");
            networkMailjTextField.setText("");
            networkMPassjTextField.setText("");
            networkMSMTPHjTextField.setText("");
            networkMSMTPPjTextField.setText("");
            this.newNetworkjButton.setVisible(false);
            updateNetworkjButton.setVisible(false);
            deleteNetworkjButton.setVisible(false);
            return;
        }
        Network newNetwork = (Network)networksjTable.getValueAt(selectedRow, 0);
        system.getNetworkList().remove(newNetwork);
        dB4OModel.storeSystem(system);
        DisplayNetworks();
    }//GEN-LAST:event_deleteNetworkjButtonActionPerformed


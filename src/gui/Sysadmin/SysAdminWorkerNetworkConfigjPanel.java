    private void DisplayNetworks() {
        DefaultTableModel model = (DefaultTableModel) networksjTable.getModel();
        model.setRowCount(0);
        for (Network network : system.getNetworkList()) {
            Object[] row = new Object[4];
            row[0] = network;
            row[1] = network.getSender();
            row[2] = network.getSmtpHost();
            row[3] = network.getSmtpPort();
            model.addRow(row);
        }
    }


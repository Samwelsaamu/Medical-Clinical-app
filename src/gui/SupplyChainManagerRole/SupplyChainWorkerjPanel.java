    private void displayMedicines(){
        DefaultTableModel model = (DefaultTableModel) enterprisessjTable.getModel();
        
        model.setRowCount(0);
        for (Medicine medicine : system.getMedicinedirectory().getMedicineList()){
            Object[] row = new Object[2];
            row[0] = medicine;
            row[1] = medicine.getQuantity();
            model.addRow(row);
        }
    }
    


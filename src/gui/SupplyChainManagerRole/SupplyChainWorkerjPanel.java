    private void saveEnterprisejButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveEnterprisejButtonActionPerformed
        String medicinename = medicineNamejTextField.getText().trim();
        String medicineqty =medicineqtyjTextField.getText().trim();
        Long qty;
        if(medicinename.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Specify Medicine Name", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(medicinename.length()<=3) {
            JOptionPane.showMessageDialog(this, "Medicine Name must be Three characters and more", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(medicinename.length()>100) {
            JOptionPane.showMessageDialog(this, "Medicine Name should have less than 100 characters", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        try{
            qty=Long.parseLong(medicineqty);
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(this, "Medicine Quantity Must be a Positive Integer Number.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(!system.getMedicinedirectory().checkIfMedicineIsUnique(medicinename)){
            JOptionPane.showMessageDialog(this, "Medicine Name Exists", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        system.getMedicinedirectory().createMedicine(medicinename, qty);
        dB4OModel.storeSystem(system);
        displayMedicines();
    }                                

